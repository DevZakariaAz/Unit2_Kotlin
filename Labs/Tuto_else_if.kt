// Tuto about Using if else in kotlin

fun main() {
    val Color = "Green"
    if (Color == "Red"){
           println("Color is Red")
    }
    else if(Color == "Green"){
        println("Color is Green")
    }
    else if(Color == "Yellow"){
        println("Color is Yellow")
    }
    else{
           println("We don't now !!")
    }
}